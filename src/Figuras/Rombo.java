package Figuras;

public class Rombo {
    int rx,ry,ranch,ralt,rcol;

    public Rombo(int rx, int ry, int ranch, int ralt, int rcol) {
        this.rx = rx;
        this.ry = ry;
        this.ranch = ranch;
        this.ralt = ralt;
        this.rcol = rcol;
    }

    public int getRx() {
        return rx;
    }

    public void setRx(int rx) {
        this.rx = rx;
    }

    public int getRy() {
        return ry;
    }

    public void setRy(int ry) {
        this.ry = ry;
    }

    public int getRAnch() {
        return ranch;
    }

    public void setRAnch(int ranch) {
        this.ranch = ranch;
    }

    public int getRAlt() {
        return ralt;
    }

    public void setRAlt(int ralt) {
        this.ralt = ralt;
    }

    public int getRCol() {
        return rcol;
    }

    public void setRCol(int rcol) {
        this.rcol = rcol;
    }
    
}
