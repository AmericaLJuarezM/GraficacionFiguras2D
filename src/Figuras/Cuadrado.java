
package Figuras;

class Cuadrado {
    int cux,cuy,cuanch,cualt,cucol;

    public Cuadrado(int cux, int cuy, int cuanch, int cualt, int cucol) {
        this.cux = cux;
        this.cuy = cuy;
        this.cuanch = cuanch;
        this.cualt = cualt;
        this.cucol = cucol;
    }

    public int getcux() {
        return cux;
    }

    public void setcux(int cux) {
        this.cux = cux;
    }

    public int getcuy() {
        return cuy;
    }

    public void setcuy(int cuy) {
        this.cuy = cuy;
    }

    public int getcuanch() {
        return cuanch;
    }

    public void setcuanch(int cuanch) {
        this.cuanch = cuanch;
    }

    public int getcualt() {
        return cualt;
    }

    public void setcualt(int cualt) {
        this.cualt = cualt;
    }

    public int getcucol() {
        return cucol;
    }

    public void setcucol(int cucol) {
        this.cucol = cucol;
    }
    
    
}
